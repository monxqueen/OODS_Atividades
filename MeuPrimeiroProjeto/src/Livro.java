public class Livro {
	private String autor;
	private double valor;
	
	public Livro(String autor, double valor) {
		this.autor = autor;
		this.valor = valor;
	}

	public String getAutor() {
		return autor;
	}

	public void setAutor(String autor) {
		this.autor = autor;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(Float valor) {
		this.valor = valor;
	}
	
	
	
}
